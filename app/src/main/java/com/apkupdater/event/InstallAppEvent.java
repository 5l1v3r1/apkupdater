package com.apkupdater.event;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

public class InstallAppEvent
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    private boolean success;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public InstallAppEvent(
        boolean success
    ) {
        this.success = success;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public boolean isSuccess() {
        return success;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

}

